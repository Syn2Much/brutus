static const uint8_t AES_RCON[11] = {0,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36};

static uint8_t aes_xtime(uint8_t a) { return (uint8_t)((a<<1)^((a&0x80)?0x1b:0)); }

void aes128_expand_key(const uint8_t key[16], uint32_t rk[44]) {
    int i;
    uint32_t t;
    for (i = 0; i < 4; i++)
        rk[i]=((uint32_t)key[i*4]<<24)|((uint32_t)key[i*4+1]<<16)|
              ((uint32_t)key[i*4+2]<<8)|(uint32_t)key[i*4+3];
    for (i = 4; i < 44; i++) {
        t = rk[i-1];
        if (i%4==0) {
            t=((uint32_t)AES_SBOX[(t>>16)&0xFF]<<24)|((uint32_t)AES_SBOX[(t>>8)&0xFF]<<16)|
              ((uint32_t)AES_SBOX[t&0xFF]<<8)|(uint32_t)AES_SBOX[(t>>24)&0xFF];
            t ^= (uint32_t)AES_RCON[i/4] << 24;
        }
        rk[i] = rk[i-4] ^ t;
    }
}

void aes128_encrypt_block(const uint32_t rk[44], uint8_t block[16]) {
    uint8_t s[16], t[16], tmp;
    int round, i, ki, cc;
    uint8_t a0,a1,a2,a3,x;
    memcpy(s, block, 16);
    for (i = 0; i < 4; i++) {
        s[i*4]^=(uint8_t)(rk[i]>>24); s[i*4+1]^=(uint8_t)(rk[i]>>16);
        s[i*4+2]^=(uint8_t)(rk[i]>>8); s[i*4+3]^=(uint8_t)(rk[i]);
    }
    for (round = 1; round <= 10; round++) {
        for (i = 0; i < 16; i++) t[i] = AES_SBOX[s[i]];
        tmp=t[1]; t[1]=t[5]; t[5]=t[9]; t[9]=t[13]; t[13]=tmp;
        tmp=t[2]; t[2]=t[10]; t[10]=tmp; tmp=t[6]; t[6]=t[14]; t[14]=tmp;
        tmp=t[15]; t[15]=t[11]; t[11]=t[7]; t[7]=t[3]; t[3]=tmp;
        if (round < 10) {
            for (cc = 0; cc < 4; cc++) {
                a0=t[cc*4]; a1=t[cc*4+1]; a2=t[cc*4+2]; a3=t[cc*4+3];
                x=a0^a1^a2^a3;
                s[cc*4]=a0^aes_xtime(a0^a1)^x; s[cc*4+1]=a1^aes_xtime(a1^a2)^x;
                s[cc*4+2]=a2^aes_xtime(a2^a3)^x; s[cc*4+3]=a3^aes_xtime(a3^a0)^x;
            }
        } else { memcpy(s, t, 16); }
        for (i = 0; i < 4; i++) {
            ki = round*4+i;
            s[i*4]^=(uint8_t)(rk[ki]>>24); s[i*4+1]^=(uint8_t)(rk[ki]>>16);
            s[i*4+2]^=(uint8_t)(rk[ki]>>8); s[i*4+3]^=(uint8_t)(rk[ki]);
        }
    }
    memcpy(block, s, 16);
}

void aes128ctr_init(aes128ctr_t *c, const uint8_t key[16], const uint8_t iv[16]) {
    aes128_expand_key(key, c->rk);
    memcpy(c->ctr, iv, 16);
    c->ks_pos = 16;
}

void aes128ctr_crypt(aes128ctr_t *c, uint8_t *data, size_t len) {
    size_t i;
    for (i = 0; i < len; i++) {
        if (c->ks_pos >= 16) {
            memcpy(c->ks, c->ctr, 16);
            aes128_encrypt_block(c->rk, c->ks);
            /* increment counter */
            {
                int j;
                for (j = 15; j >= 0; j--) if (++c->ctr[j] != 0) break;
            }
            c->ks_pos = 0;
        }
        data[i] ^= c->ks[c->ks_pos++];
    }
}
